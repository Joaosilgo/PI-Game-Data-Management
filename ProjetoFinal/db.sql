drop database if exists `pi1718`;
CREATE DATABASE IF NOT EXISTS `pi1718`;
USE `pi1718`;

DROP TABLE IF EXISTS `person`;
DROP TABLE IF EXISTS `country`;
DROP TABLE IF EXISTS `session`;
DROP TABLE IF EXISTS `stat`;

CREATE TABLE `country` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `name` varchar(255) NOT NULL,
  `shortName` varchar(255) NOT NULL,
  PRIMARY KEY (`id`)
);

CREATE TABLE `person` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `name` varchar(255) NOT NULL,
  `birthDate` date NOT NULL,
  `idCountry` int(11) NOT NULL,
  `timeSpan` int(11) NOT NULL,
  
  
  PRIMARY KEY (`id`)
);
CREATE TABLE `session` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `descriptionSession` varchar(255) NOT NULL,
  `assignationSession` date NOT NULL,
  `idCountry` int(11) NOT NULL,
   `timeSpan` int(11) ,
  
  PRIMARY KEY (`id`)
);


CREATE TABLE `stat` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `wins` int(11) NOT NULL,
  `loses` int(11) NOT NULL,
  `idPerson` int(11) NOT NULL,
  
  
  
  PRIMARY KEY (`id`)
);

ALTER TABLE `person`
	ADD CONSTRAINT `person_country` FOREIGN KEY (`idCountry`) REFERENCES `country` (`id`);
    ALTER TABLE `session`
	ADD CONSTRAINT `session_country` FOREIGN KEY (`idCountry`) REFERENCES `country` (`id`);
    ALTER TABLE `stat`
	ADD CONSTRAINT `stat_person` FOREIGN KEY (`idPerson`) REFERENCES `person` (`id`);

